/**
 * One-time data migration: Neon Postgres (old Next.js stack) -> D1 SQL file.
 *
 * Usage:
 *   npm i -g postgres   (or run from a dir with `postgres` installed)
 *   DATABASE_URL="postgres://..." node scripts/migrate-from-neon.mjs > migration-data.sql
 *   wrangler d1 execute ai-video-db --remote --file=migration-data.sql
 *
 * Notes (plan §7):
 * - Sessions are NOT migrated (users re-login once).
 * - Timestamps convert to unix ms integers.
 * - Old renderJobs serial ids become "legacy-<n>" text ids.
 * - Asset URLs (S3) are left as-is; run the R2 URL rewrite after rclone copy.
 */
import postgres from "postgres";

const sql = postgres(process.env.DATABASE_URL, { ssl: "require" });

const q = (v) => {
	if (v === null || v === undefined) return "NULL";
	if (typeof v === "number") return String(v);
	if (typeof v === "boolean") return v ? "1" : "0";
	if (v instanceof Date) return String(v.getTime());
	if (typeof v === "object") return q(JSON.stringify(v));
	return `'${String(v).replace(/'/g, "''")}'`;
};
const row = (table, cols, vals) =>
	`INSERT OR IGNORE INTO ${table} (${cols.join(",")}) VALUES (${vals.map(q).join(",")});`;

const out = (s) => process.stdout.write(s + "\n");

out("-- Generated by migrate-from-neon.mjs on " + new Date().toISOString());
out("PRAGMA defer_foreign_keys = true;");

for (const u of await sql`SELECT * FROM "user"`) {
	out(row("user",
		["id","name","email","email_verified","image","tokens","is_admin","created_at","updated_at"],
		[u.id,u.name,u.email,u.email_verified,u.image,u.tokens,u.is_admin,u.created_at,u.updated_at]));
}

for (const a of await sql`SELECT * FROM account`) {
	out(row("account",
		["id","account_id","provider_id","user_id","access_token","refresh_token","id_token",
		 "access_token_expires_at","refresh_token_expires_at","scope","password","created_at","updated_at"],
		[a.id,a.account_id,a.provider_id,a.user_id,a.access_token,a.refresh_token,a.id_token,
		 a.access_token_expires_at,a.refresh_token_expires_at,a.scope,a.password,a.created_at,a.updated_at]));
}

for (const p of await sql`SELECT * FROM projects`) {
	out(row("projects",
		["id","name","script","current_step","ratio","length","language","voice","voiceover_url",
		 "timestamps","scenes","composition","caption_config","schema_version","user_id","created_at","updated_at"],
		[p.id,p.name,p.script,p.current_step,p.ratio,p.length,p.language,p.voice,p.voiceover_url,
		 p.timestamps,p.scenes,p.composition,p.caption_config,1,p.user_id,p.created_at,p.updated_at]));
}

for (const t of await sql`SELECT * FROM token_transactions`) {
	out(row("token_transactions",
		["id","user_id","amount","type","description","project_id","created_at"],
		[t.id,t.user_id,t.amount,t.type,t.description,t.project_id,t.created_at]));
}

for (const c of await sql`SELECT * FROM token_costs`) {
	out(row("token_costs",
		["id","action","cost","description","is_active","created_at","updated_at"],
		[c.id,c.action,c.cost,c.description,c.is_active,c.created_at,c.updated_at]));
}

for (const n of await sql`SELECT * FROM notifications`) {
	out(row("notifications",
		["id","user_id","type","title","message","project_id","project_name","download_url","is_read","email_sent","created_at"],
		[n.id,n.user_id,n.type,n.title,n.message,n.project_id,n.project_name,n.download_url,n.is_read,n.email_sent,n.created_at]));
}

for (const r of await sql`SELECT * FROM render_jobs`) {
	out(row("render_jobs",
		["id","user_id","project_id","resolution","status","video_url","progress","error","created_at","updated_at"],
		[`legacy-${r.id}`,r.user_id,r.project_id,"720p",r.status,r.video_url,r.progress ?? 0,r.error,r.created_at,r.updated_at]));
}

for (const s of await sql`SELECT * FROM settings`) {
	out(row("settings",
		["id","default_signup_bonus","minimum_token_balance","enable_token_system","enable_signup_bonus",
		 "max_tokens_per_user","token_expiration_days","created_at","updated_at"],
		[s.id,s.default_signup_bonus,s.minimum_token_balance,s.enable_token_system,s.enable_signup_bonus,
		 s.max_tokens_per_user,s.token_expiration_days,s.created_at,s.updated_at]));
}

out("PRAGMA defer_foreign_keys = false;");
await sql.end();
